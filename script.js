let currentLang = localStorage.getItem("siteLang") || "en";

function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("siteLang", lang);

  const t = translations[lang];
  if (!t) return;

  // Update all elements with data-lang-key
  document.querySelectorAll("[data-lang-key]").forEach((el) => {
    const key = el.getAttribute("data-lang-key");
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  // Update placeholders
  document.querySelectorAll("[data-placeholder-key]").forEach((el) => {
    const key = el.getAttribute("data-placeholder-key");
    if (t[key] !== undefined) {
      el.placeholder = t[key];
    }
  });

  // Update lang toggle button
  const langToggle = document.getElementById("lang-toggle");
  if (langToggle) {
    const flagEl = langToggle.querySelector(".lang-flag");
    const labelEl = langToggle.querySelector(".lang-label");
    if (lang === "en") {
      if (flagEl) flagEl.textContent = "🇬🇧";
      if (labelEl) labelEl.textContent = "EN";
    } else {
      if (flagEl) flagEl.textContent = "🇮🇩";
      if (labelEl) labelEl.textContent = "ID";
    }
  }

  // Update active state on dropdown options
  document.querySelectorAll(".lang-option").forEach((btn) => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
  });

  // Update html lang attribute
  document.documentElement.lang = lang === "id" ? "id" : "en";

  // Re-init word animation after language change (words update)
  initWordAnimation();
}

function initLangSwitcher() {
  const langToggle = document.getElementById("lang-toggle");
  const dropdown = document.querySelector(".lang-dropdown");

  if (!langToggle || !dropdown) return;

  langToggle.addEventListener("click", (e) => {
    e.stopPropagation();
    const isMobile = window.innerWidth <= 768;
    if (isMobile) {
      // On mobile, toggle inline dropdown (CSS display: none / flex)
      const isOpen = dropdown.classList.contains("open");
      dropdown.classList.toggle("open", !isOpen);
      langToggle.classList.toggle("active", !isOpen);
    } else {
      dropdown.classList.toggle("open");
      langToggle.classList.toggle("active");
    }
  });

  document.querySelectorAll(".lang-option").forEach((btn) => {
    btn.addEventListener("click", (e) => {
      e.stopPropagation();
      const lang = btn.getAttribute("data-lang");
      applyLanguage(lang);
      dropdown.classList.remove("open");
      langToggle.classList.remove("active");
    });
  });

  // Close dropdown on outside click (desktop only)
  document.addEventListener("click", (e) => {
    if (!langToggle.contains(e.target) && !dropdown.contains(e.target)) {
      dropdown.classList.remove("open");
      langToggle.classList.remove("active");
    }
  });

  // Re-position on resize
  window.addEventListener("resize", () => {
    dropdown.classList.remove("open");
    langToggle.classList.remove("active");
  });
}

/* =============================================
   WORD ANIMATION
   ============================================= */
let wordAnimInterval = null;

function initWordAnimation() {
  // Clear old interval
  if (wordAnimInterval) {
    clearInterval(wordAnimInterval);
    wordAnimInterval = null;
  }

  let words = document.querySelectorAll(".word");

  // Re-build letter spans from current text content
  words.forEach((word) => {
    // Get existing text (could be spans already)
    let text = "";
    word.childNodes.forEach((node) => {
      if (node.nodeType === Node.TEXT_NODE) text += node.textContent;
      else if (node.tagName === "SPAN") text += node.textContent;
    });
    word.textContent = "";
    text.split("").forEach((letter) => {
      let span = document.createElement("span");
      span.textContent = letter;
      span.className = "letter";
      word.append(span);
    });
  });

  if (words.length === 0) return;

  let currentWordIndex = 0;
  let maxWordIndex = words.length - 1;
  words.forEach((w) => (w.style.opacity = "0"));
  words[0].style.opacity = "1";

  let changeText = () => {
    let currentWord = words[currentWordIndex];
    let nextWord =
      currentWordIndex === maxWordIndex
        ? words[0]
        : words[currentWordIndex + 1];

    Array.from(currentWord.children).forEach((letter, i) => {
      setTimeout(() => {
        letter.className = "letter out";
      }, i * 80);
    });

    nextWord.style.opacity = "1";
    Array.from(nextWord.children).forEach((letter, i) => {
      letter.className = "letter behind";
      setTimeout(
        () => {
          letter.className = "letter in";
        },
        340 + i * 80,
      );
    });

    currentWordIndex =
      currentWordIndex === maxWordIndex ? 0 : currentWordIndex + 1;
  };

  changeText();
  wordAnimInterval = setInterval(changeText, 3000);
}

