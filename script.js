/* =============================================
   EXPERIENCE PHOTO SLIDER
   ============================================= */
(function () {
  const track = document.getElementById("expSliderTrack");
  const prevBtn = document.getElementById("expPrev");
  const nextBtn = document.getElementById("expNext");
  const dots = document.querySelectorAll(".exp-dot");

  if (!track || !prevBtn || !nextBtn) return;

  const slides = track.querySelectorAll(".exp-slide");
  const total = slides.length;
  let current = 0;
  let autoTimer = null;

  function goTo(idx) {
    if (idx < 0) idx = total - 1;
    if (idx >= total) idx = 0;
    current = idx;
    track.style.transform = `translateX(-${current * 100}%)`;
    dots.forEach((dot, i) => {
      dot.classList.toggle("active", i === current);
    });
  }

  function startAuto() {
    stopAuto();
    autoTimer = setInterval(() => goTo(current + 1), 4000);
  }

  function stopAuto() {
    if (autoTimer) clearInterval(autoTimer);
  }

  prevBtn.addEventListener("click", () => {
    goTo(current - 1);
    stopAuto();
    startAuto();
  });
  nextBtn.addEventListener("click", () => {
    goTo(current + 1);
    stopAuto();
    startAuto();
  });

  dots.forEach((dot) => {
    dot.addEventListener("click", () => {
      const idx = parseInt(dot.getAttribute("data-idx"), 10);
      goTo(idx);
      stopAuto();
      startAuto();
    });
  });

  let touchStartX = 0;
  track.addEventListener(
    "touchstart",
    (e) => {
      touchStartX = e.changedTouches[0].screenX;
    },
    { passive: true },
  );
  track.addEventListener(
    "touchend",
    (e) => {
      const diff = touchStartX - e.changedTouches[0].screenX;
      if (Math.abs(diff) > 40) {
        goTo(diff > 0 ? current + 1 : current - 1);
        stopAuto();
        startAuto();
      }
    },
    { passive: true },
  );

  goTo(0);
  startAuto();
})();

